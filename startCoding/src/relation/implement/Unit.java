package relation.implement;

public class Unit {
	private int hp;
	private int mana;
	
	public Unit(int hp, int mana) {
		this.hp = hp;
		this.mana = mana;
	}
	
	public void setHp(int hp) {
		this.hp = hp;
	}
	public int getHp() {
		return this.hp;
	}
	public void setMana(int mana) {
		this.mana = mana;
	}
	public int getMana() {
		return this.mana;
	}
}






